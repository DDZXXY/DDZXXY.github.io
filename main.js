const textNodes = document.querySelectorAll("[data-i18n]");
const listMounts = document.querySelectorAll("[data-render-list]");
const tagMounts = document.querySelectorAll("[data-render-tags]");
const toggle = document.querySelector(".language-toggle");
const langLabel = document.querySelector("[data-lang-label]");

function getStoredLanguage() {
  try {
    return localStorage.getItem("site-language") || "en";
  } catch (error) {
    return "en";
  }
}

function storeLanguage(language) {
  try {
    localStorage.setItem("site-language", language);
  } catch (error) {
    return;
  }
}

let currentLanguage = getStoredLanguage();

function createListItem(item) {
  const article = document.createElement("article");
  article.className = "timeline-item";

  const meta = document.createElement("div");
  meta.className = "timeline-meta";
  meta.innerHTML = `<span>${item.date}</span><span>${item.location}</span>`;

  const contentBlock = document.createElement("div");
  contentBlock.className = "timeline-content";

  const title = document.createElement("h3");
  title.textContent = item.title;

  const organization = document.createElement("p");
  organization.textContent = item.organization;

  const bullets = document.createElement("ul");
  item.bullets.forEach((bullet) => {
    const li = document.createElement("li");
    li.textContent = bullet;
    bullets.appendChild(li);
  });

  contentBlock.append(title, organization, bullets);
  article.append(meta, contentBlock);
  return article;
}

function createCard(item) {
  const article = document.createElement("article");
  article.className = "info-card";
  article.innerHTML = `
    <h3>${item.title}</h3>
    <p>${item.subtitle}</p>
    <p class="meta">${item.meta}</p>
    <p>${item.body}</p>
  `;
  return article;
}

function createLeadershipItem(item) {
  const article = document.createElement("article");
  article.className = "leadership-item";
  article.innerHTML = `
    <h3>${item.title}</h3>
    <p class="meta">${item.meta}</p>
    <p>${item.body}</p>
  `;
  return article;
}

function renderList(mount, key, languageContent) {
  mount.replaceChildren();
  languageContent[key].forEach((item) => {
    if (key === "education") {
      mount.appendChild(createCard(item));
      return;
    }

    if (key === "leadership") {
      mount.appendChild(createLeadershipItem(item));
      return;
    }

    mount.appendChild(createListItem(item));
  });
}

function renderTags(mount, key, languageContent) {
  mount.replaceChildren();
  languageContent[key].forEach((item) => {
    const tag = document.createElement("span");
    tag.className = "tag";
    tag.textContent = item;
    mount.appendChild(tag);
  });
}

function setLanguage(language) {
  const languageContent = content[language];
  currentLanguage = language;
  document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
  document.title = languageContent.pageTitle;

  textNodes.forEach((node) => {
    const key = node.dataset.i18n;
    node.textContent = languageContent[key];
  });

  listMounts.forEach((mount) => {
    renderList(mount, mount.dataset.renderList, languageContent);
  });

  tagMounts.forEach((mount) => {
    renderTags(mount, mount.dataset.renderTags, languageContent);
  });

  langLabel.textContent = language === "en" ? "中文" : "EN";
  toggle.setAttribute("aria-pressed", String(language === "zh"));
  storeLanguage(language);
}

toggle.addEventListener("click", () => {
  setLanguage(currentLanguage === "en" ? "zh" : "en");
});

setLanguage(currentLanguage);
